// Declara a função que calcula a potência usando o operador de exponenciação
function calculaPotencia(base, expoente) {
    return base ** expoente;
}

// Testa a função com alguns exemplos
let resultado1 = calculaPotencia(2, 3);  // 2 elevado a 3
let resultado2 = calculaPotencia(5, 4);  // 5 elevado a 4

// Exibe os resultados no console
console.log("2 elevado a 3 é: " + resultado1);
console.log("5 elevado a 4 é: " + resultado2);